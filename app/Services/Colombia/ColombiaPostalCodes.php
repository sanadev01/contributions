    public function getServiceByPostalCode($postalCode)
    {
        if ($this->nacionalService()->contains($postalCode)) {
            return ShippingService::COLOMBIA_NACIONAL;
        }

        if ($this->urbanoService()->contains($postalCode)) {
            return ShippingService::COLOMBIA_URBANO;
        }

        if ($this->trayetoService()->contains($postalCode)) {
            return ShippingService::COLOMBIA_TRAYETOS;
        }
        return null;
    }

    public function getPostalData()
    {
        $data = [];
        $cities = $this->colombiaCities()->toArray();
        $data = array_merge(['cities' => $cities]);
        return $data;
    }

    public function getDepartment($city)
    {
        $department = [];
        $cities = $this->colombiaCities();
        $depts = $this->colombiaDepts();
        $data = $cities->zip($depts)->toArray();
        foreach ($data as $key => $val) {
            if ($val[0] === $city) {
                array_push($department, $val[1]);
            }
        }
        return $department;
    }

